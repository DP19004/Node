                private void guardarSubArbol() 
                {
                Nodo op2 = (Nodo) pOperandos.pop();
                Nodo op1 = (Nodo) pOperandos.pop();
                pOperandos.push(new Nodo( pOperadores.pop(), op1 , op2));
                }
    
