                public void Inorder(Nodo n)
                {
                    if(n!=null){
                    derivada.append(n.getData()  + "");
                    Inorder(n.getRight());
                    Inorder(n.getLeft());
                   
                    
                    }
                }
                
    //construir nodo root
        public  Nodo construct(String cadena)
        {
           StringTokenizer tokenizer; //Permite dividir la cadena ingresada
           String token; //Para almacenar el token a analizar
           tokenizer = new StringTokenizer (cadena , blanco + operadores, true);
             //Divide la expresi칩n ingresada y las separa, evaluando los delimitadores como tokens
             while (tokenizer.hasMoreTokens ()) 
             { //Mientra existan m치s tokens disponibles
                 token = tokenizer.nextToken(); //Devuelve el siguiente token del objeto StringTokenizer.
                 if (blanco.indexOf (token) >= 0)
                     ; // Si el token es un espacio en blanco, se ignora
                 else if (operadores.indexOf (token) < 0)
                 {
                     
                     // Si el token es un operando, lo guarda como nodo del arbol
                    Nodo a;
                    pOperandos.push (new Nodo (token));
                 } 
                 else 
                     if(token.equals(")"))
                     {
                         while(!pOperadores.empty() && !pOperadores.peek().equals("("))
                             guardarSubArbol();
                      pOperadores.pop();
                         
                     } 
                     else
                     {
                          if(!token.equals("(") && !pOperadores.empty())
                          {
                            
                           String op = (String) pOperadores.peek();
                           while(op.equals("(") && !pOperadores.empty() && operadores.indexOf(op)>= operadores.indexOf(token))
                           {
                               guardarSubArbol();
                               if(!pOperadores.empty())
                                   op = (String) pOperadores.peek();
                           }
                          
                          }
                          pOperadores.push(token);
                     }
                         
             }
             raiz = (Nodo) pOperandos.peek();
              while(!pOperadores.empty())
              {
                  if(pOperadores.peek().equals("("))
                      pOperadores.pop();
                  else
                  {
                      guardarSubArbol();
                      raiz = (Nodo) pOperandos.peek();


                  }
                  
              }
             
             
             
             return raiz;
        }
    
    
    
    
    
    
